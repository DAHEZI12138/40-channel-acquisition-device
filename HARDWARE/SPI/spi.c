u8 SPI2_ReadWriteByte(u8 TxData)
{
	u8 Rxdata;
	HAL_SPI_TransmitReceive(&hspi1,&TxData,&Rxdata,1, 100);      
	return Rxdata; 
}

void SPI2_SetSpeed(u32 SPI_BaudRatePrescaler)
{
    assert_param(IS_SPI_BAUDRATE_PRESCALER(SPI_BaudRatePrescaler));
    __HAL_SPI_DISABLE(&hspi1);            //close SPI
    hspi1.Instance->CFG1&=~(0X7<<28);     //Setting the baud rate
    hspi1.Instance->CFG1|=SPI_BaudRatePrescaler;//set SPI speed
    __HAL_SPI_ENABLE(&hspi1);             //enable SPI
    
}



